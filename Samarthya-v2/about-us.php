            <h2 style="color:#FE9A2E" >About MGNREGA </h2>
            <p>
                The National Rural Employment Guarantee Act (Mahatma Gandhi NREGA) notified 
                on September 7, 2005, aims at enhancing livelihood security of households in rural 
                areas of the country by providing at least one hundred days of guaranteed wage 
                employment in a financial year to every household whose adult members volunteer 
                to do unskilled manual work. The Act covers all rural districts of the country.
            </p>
            <p>
                <b> As per the Para 3 of Schedule-I, MGNREGA</b><br/><br/>

                <b>The core objectives of the Schemes are:</b><br/><br/>

Providing not less than one hundred days of unskilled manual work as a guaranteed 

employment in a financial year to every household in rural areas as per demand, 

resulting in creation of productive assets of prescribed quality and durability

 Strengthening the livelihood resources base of the poor

Proactively ensuring social inclusion and

Strengthening Panchayat Raj Institution.<br/><br/>

<b>1.2. LIST OF WORKS PERMITTED UNDER THE ACT:</b><br/><br/>

<b>At para 4. (1) of Schedule – I, Mahatma Gandhi NREGA, it is mentioned that, “The 

focus of the scheme shall be on the following works as categorized below”<br/>

Category A: Public works relating to natural resources management:</b><br/><br/>

<b>Water conservation </b>and water harvesting structures to augment and improve 

groundwater like underground dykes, earthen dams, stop dams, check dams with 

special focus on recharging ground water including drinking water sources<br/><br/>

<b>Watershed management</b> works such as contour trenches, terracing, contour bunds, 

boulder checks, gabion structures and spring shed development resulting in a 

comprehensive treatment of a watershed<br/><br/>

Micro and minor irrigation works and creation, renovation and maintenance of 

irrigation canals and drains<br/><br/>

Renovation of <b>traditional water bodies</b> including desilting of irrigation tanks and 

other water bodies<br/><br/>

<b>Afforestation</b>, tree plantation and horticulture in common and forest lands, road 

margins, canal bunds, tank foreshores and coastal belts duly providing right to usufruct 

to the households covered in Paragraph 5; and<br/><br/>

Land development works in common land.<br/><br/>

Category B: Community assets or individual assets for vulnerable sections (only 

for households in paragraph 5):

Improving productivity of lands of households specified in Paragraph 5 through 

land development and by providing suitable infrastructure for irrigation including dug 

wells, farm ponds and other water harvesting structures;

Improving livelihoods through horticulture, sericulture, plantation, and farm forestry;

Development of fallow or waste lands of households defined in Paragraph 5 to bring it 

under cultivation;

Unskilled wage component in construction of houses sanctioned under the Indira 

Awas Yojana or such other State or Central Government Scheme;

Creating infrastructure for promotion of livestock such as, poultry shelter, goat 

shelter, piggery shelter, cattle shelter and fodder troughs for cattle; and

Creating infrastructure for promotion of fisheries such as, fish drying yards, storage 

facilities, and promotion of fisheries in seasonal water bodies on public land;

Category C: common infrastructure including for NRLM compliant self-help 

groups:

Works for promoting agricultural productivity by creating durable infrastructure 

required for biofertilizers and post-harvest facilities including pucca storage 

facilities for agricultural produce;

Common work-sheds for livelihood activities of self-help groups.

Category D: Rural infrastructure:

Rural sanitation related works, such as, individual household latrines, school toilet 

units, Anganwadi toilets either independently or in convergence with schemes of 

other Government Departments to achieve ‘open defecation free’ status. and solid 

and liquid waste management as per prescribed norms.

Providing all-weather rural road connectivity to unconnected villages and to connect 

identified rural production centers to the existing pucca road network; and 

construction of pucca internal roads or streets including side drains and culverts 

within a village;

Construction of play fields;

Works for improving disaster preparedness or restoration of roads or restoration 

of other essential public infrastructure including flood control and protection 

works, providing drainage in water logged areas, deepening and repairing of flood 

channels, chaur renovation, construction of storm water drains for coastal protection;

Construction of buildings for Gram Panchayats, women self-help groups’ federations, 

cyclone shelters, Anganwadi centers, village haats and crematoria at the village or 

block level.

Construction of Food Grain Storage Structures for implementing the provisions of 

The National Food Security Act 2013 (20 of 2013);

Production of building material required for construction works under the Act as a 

part of the estimate of such construction works.

Maintenance of rural public assets created under the Act;

4. (2) The order of priority of works shall be determined by each Gram Panchayat 

in the meeting of the Gram Sabha keeping in view potential of the local area, its needs, 

local resources and in accordance with the provisions of Paragraph 9, provided that 

the District Programme Coordinator shall ensure that at least 60% of works to be 

taken up in a district in terms of cost shall be for creation of productive assets 

directly linked to agriculture and allied activities through development of land, 

water & trees.

4.(3) Works which are non-tangible, not measurable, repetitive such as, removing 

grass, pebbles, agricultural operations, shall not be taken up

5. Works creating individual assets shall be prioritised on land or homestead owned 

by households belonging to the:

Scheduled Castes

Scheduled Tribes

Nomadic tribes

De-notified tribes

Other families below the poverty line

Women-headed households

Physically handicapped headed households

Beneficiaries of land reforms

The beneficiaries under the Indira Awaas Yojana

Beneficiaries under the Scheduled Tribes and Other Traditional Forest Dwellers 

(Recognition of Forest Rights) Act, 2006 (2 of 2007) and after exhausting the eligible 

beneficiaries under the above categories, on lands of the small or marginal farmers as 

defined in the Agriculture Debt Waiver and Debt Relief Scheme, 2008 subject to the 

condition that such households shall have a job card with at least one member willing 

to work on the project undertaken on their land or homestead.

6. The State Government shall take concrete steps to achieve effective inter-

departmental convergence till the last mile implementation level of the works under 

the Scheme with other Government Schemes/ programmes so as to improve the 

quality and productivity of assets, and bring in synergy to holistically address the 

multiple dimensions of poverty in a sustainable manner.

There shall be a systematic, participatory planning exercise at each tier of Panchayat, 

conducted between August to December month of every year, as per a detailed 

methodology laid down by the State Government. All works to be executed by the 

Gram Panchayats shall be identified and placed before the Gram Sabha and such 

works which are to be executed by the intermediate Panchayats or other 

implementing agencies shall be placed before the intermediate or District 

Panchayats, along with the expected outcomes.

(1) Adequate shelf of works shall be maintained by every Gram Panchayat to meet 

the expected demand for work in such a way that at least one labour intensive 

public work with at least one work which is suitable for Particularly Vulnerable 

Groups especially the aged and the disabled which shall be kept open at all times to 

provide work as per demand.

(2) The details of the said work(s) shall be prominently displayed through 

writings on the walls of the village.

While opening works in the public works category, it shall be ensured that the 

ongoing or incomplete works should be completed first.

Work shall be provided within fifteen days, from the date of registration of the 

demand for work or the date from which work has been demanded in case of advance 

applications, whichever is later.

(1) In case work could not be provided as per demand within the specified 

time limit,

unemployment allowance shall be paid, as calculated automatically by the 

computer system or the Management Information System and as provided 

under the Act. The programme officer can reject the unemployment allowance 

only on grounds of force majeure.

(2) In cases where unemployment allowance is paid, or due to be paid, the 

Programme Officer shall inform the concerned District Programme 

Coordinator in writing the reasons for not providing employment to the 

applicants.

(3) The District Programme Coordinator shall, in his Annual Report to the 

State Council explain as to why employment could not provide in cases 

where payment of unemployment allowances is involved

12. Every work under the Scheme shall have a technical estimate duly sanctioned by 

an authority authorised by the State Government. While sanctioning the estimates, 

the following are required to be considered:

For all works involving construction, cost effective, labour intensive technologies and 

usage of local materials shall be employed as far as possible;

The Bill of quantities (used in the estimate) is stated in common terminology for 

easy understanding of all stakeholders;

Each work shall have a summary of the estimate, design and a technical note that indicate 

the expected outcomes from implementing the work.

The works finalised at the Gram Panchayat level and consolidated at the Block or 

District level shall be given Block-wise administrative or financial sanction by the 

competent authority within thirty days from the date of finalisation of the works at 

the Gram Panchayat level only after confirming that the shelf of works in any Gram 

Panchayat is not less than two times the labour budget approved for that Gram 

Panchayat.

Payment shall only be made based on the measurements taken at the worksite by the 

authorised personnel within three days of closure of the muster roll. The State 

Government shall ensure that adequate technical personnel are deployed to complete 

this work within the stipulated period. Suitable persons from the families of workers 

may be trained or skilled and deployed as barefoot engineers with appropriate 

delegation of technical powers and paid wages as skilled workers.

The State Government shall link the wages, without any gender bias, with the 

quantity of work done and it shall be paid according to the rural schedule of rates 

fixed after time and motion studies for different types of work and different seasons 

and revised periodically.

A separate Schedule of rates shall be finalised for women, the elderly, people with 

disabilities and people with debilitating ailments so to improve their participation 

through productive work.

(a) The schedule of rates of wages for various unskilled labourers shall be fixed up so 

that an adult person worked for eight hours which include an hour of rest will earn a 

wage which is equal to the stipulated wage rate;

(b) The working hours of an adult worker shall be flexible but shall not spread over 

more than twelve hours on any day.

For all works taken up by the Gram Panchayats, the cost of the material component 

including the wages of the skilled and semi-skilled workers shall not exceed forty 

per cent at the Gram Panchayat level. For works taken up by the implementing 

agencies other than Gram Panchayats, the overall material component including the 

wages of the skilled and semi-skilled workers shall not exceed forty percent at the 

District level.

The works executed shall be done without engaging any contractor. Implementing 

agencies under the Scheme shall execute the works in conformity with the processes 

specified under the Act and after complying with the mandatory proactive disclosures 

and social audit.

As far as practicable, works executed by the programme implementation agencies shall 

be performed by using manual labour and no labour displacing machines shall be used.

All material required for the works shall be procured by the Gram Panchayat or the 

implementing agency using a transparent tender process as specified by the State 

Government.

Out of the administrative costs allowed under the Scheme, at least one third (1/3rd) 

shall be utilized at the Gram Panchayat level to employ and pay the Gram Rozgar 

Sahayak ,other technical personnel as per the work done and for other administrative 

expenses.

IMPOTANT INSTRUCTIONS ISSUED ON MGNREGA WORKS FROM 

THE MINISTRY:

The Sub Para (1) of Paragraph 4 of Schedule 1, MGNREGA modified as on 21st July, 

2014, lays down that “Provided that the District Programme Coordinator shall ensure 

that at least 60% of the works to be taken up in a district in terms of cost shall be 

for creation of productive asset directly linked to agriculture and allied activities 

through development of land, water and trees”.

To clarify on MGNREGA works, directly linked to agriculture and allied activities 

through development of land, water and trees, the clarification vide Ministry letter No. 

J-11017/41/2012-MGNREGA (UN) (Pt-II), dated 17th September, 2014 has been 

issued that all works of category A, all works of category B &C, except one type of 

work in each category and work at Para (vi) of category D are directly linked to 

agriculture and allied activities through development of land, water and trees.

The Paragraph 22 of Schedule 1 MGNREGA lays down that “As far as practicable, 

works executed by the programme implementation agencies shall be performed by 

using manual labour and no labour displacing machines shall be used”.

With this, there was confusion that machines cannot be used. In this regard, vide 

Ministry letter No. J-11011/09/2014-RE-I, dated 25th August, 2014 clarification & a 

suggestive list of such task and type of machines which can be used under MGNREGA 

have been issued.

Vide Notification dated 3rd January 2014, Ministry of Rural Development, 

Government of India has expanded the scope of works listed in Schedule 1, Para 4 (i) 

of MGNREGA and included number of material intensive works such as construction 

of rural buildings, infrastructure for promotion of Livestock & agriculture 

productivity etc., whereas, as per Para 20 of Schedule I, of MGNREGA, the 60:40 ratio 

for wage and material costs is required to be maintained at GP level for all works to 

be taken up by GP and for works to be taken up by all other agencies it has to be 

maintained at the District level.

To increase labour component in construction of buildings & other infrastructures, 

production of building material required in execution of MGNREGA works, as one of 

the activities under MGNREGA has been included in schedule-1, MGNREGA at Para 

4.(1) (iv) (vii). In pursuant to this, the guidelines for production of building material 

has been issued vide Ministry letter No. J-11017/26/2008-MGNREGA (UN) dated 13th 

January, 2014.

Para 13. Of Schedule-1, MGNREGA, 2005 provides that each work proposed to be 

taken up under MGNREGA shall have a summary of the estimate, design and a 

technical note that indicate the expected outcomes from implementing the work. 

Therefore, in this regard Ministry has further issued instructions on outcome 

orientation in works under MGNREGA vide Ministry letter No. J-

11011/02/2010-MGNREGA (Policy) (10093), dated 5th August, 2014.

Many other important instructions issued on MGNREGA works, time to time from the 

ministry have been referred in respective chapters of this manual.
